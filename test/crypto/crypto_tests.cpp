#include <gtest/gtest.h>
#include <cryptcrypto.h>

TEST(Crypto, Hash)
{
  EXPECT_FALSE(CryptCrypto::hash("Hello") == CryptCrypto::hash("hello"));
  EXPECT_EQ(CryptCrypto::hash("hello"), CryptCrypto::hash("hello"));
  EXPECT_FALSE(CryptCrypto::hash("Hello") == CryptCrypto::hash("hEllo"));
  EXPECT_FALSE(CryptCrypto::hash("hello") == CryptCrypto::hash("hello1"));
  EXPECT_EQ(CryptCrypto::hash("new"), CryptCrypto::hash("new"));
  std::string long_str1 = "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000";
  std::string long_str2 = "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000"
                          "000000000000000000000000000000000000000000000000000000";
  EXPECT_EQ(CryptCrypto::hash(long_str1), CryptCrypto::hash(long_str2));
}